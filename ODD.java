package addontask;

public class productofoddintegers {

	
	    public static void main(String[] args) {
	        int product = 1;

	        // Loop through odd numbers from 1 to 15
	        for (int i = 1; i <= 15; i += 2) {
	            product *= i;
	        }

	        // Print the result
	        System.out.println("Product of odd numbers from 1 to 15 = " + product);
	    }
	}


